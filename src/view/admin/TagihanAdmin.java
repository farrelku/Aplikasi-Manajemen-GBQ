   public void tampilMahasiswa() {

    DefaultTableModel model = new DefaultTableModel();

    model.addColumn("ID");
    model.addColumn("Nama");
    model.addColumn("NIM");

    tabelCariMahasiswa.setModel(model);

    try {

        String sql = "SELECT * FROM mahasiswa";

        pst = con.prepareStatement(sql);

        rs = pst.executeQuery();

        while (rs.next()) {

            model.addRow(new Object[]{

                rs.getString("id"),
                rs.getString("nama"),
                rs.getString("nim")

            });

        }

    } catch (Exception e) {

        JOptionPane.showMessageDialog(null, e.getMessage());

    }

}
   public void tampilData() {

    DefaultTableModel model = new DefaultTableModel();


    model.addColumn("Nama");
    model.addColumn("NIM");
    model.addColumn("Bulan");
    model.addColumn("Jumlah");
    model.addColumn("Status");

    tabelTagihanAdmin.setModel(model);

    try {

        String sql = "SELECT * FROM tagihan";

        pst = con.prepareStatement(sql);

        rs = pst.executeQuery();

        while (rs.next()) {

            model.addRow(new Object[]{

             
                rs.getString("nama"),
                rs.getString("nim"),
                rs.getString("bulan"),
                new DecimalFormat("#,###").format(rs.getDouble("jumlah")),
                rs.getString("status")

            });

        }

    } catch (Exception e) {

        JOptionPane.showMessageDialog(null, e);

    }

}
