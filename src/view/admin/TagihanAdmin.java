    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // TODO add your handling code here:
            try {

        String sql = "INSERT INTO tagihan "
                + "(nama,nim,bulan,jumlah,status) "
                + "VALUES (?,?,?,?,?)";

        pst = con.prepareStatement(sql);

        // hapus koma dulu
        String jumlahText = editJumlah.getText().replace(",", "");

        pst.setString(1, editNama.getText());
        pst.setString(2, editNIM.getText());
        pst.setString(3, dropdownBulan.getSelectedItem().toString());

        // simpan sebagai double
        pst.setDouble(4, Double.parseDouble(jumlahText));

        pst.setString(5, editStatus.getSelectedItem().toString());

        pst.executeUpdate();

        JOptionPane.showMessageDialog(
                null,
                "Data berhasil ditambahkan");

        tampilData();

        // reset form
        editNama.setText("");
        editNIM.setText("");
        editJumlah.setText("");

        dropdownBulan.setSelectedIndex(0);
        editStatus.setSelectedIndex(0);

    } catch (Exception e) {

        JOptionPane.showMessageDialog(
                null,
                e.getMessage());

    }
    }//GEN-LAST:event_btnTambahActionPerformed

