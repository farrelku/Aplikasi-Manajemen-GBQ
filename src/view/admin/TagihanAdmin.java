    private void btnCariTagihanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariTagihanActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model =
new DefaultTableModel();

model.addColumn("Nama");
model.addColumn("Nim");
model.addColumn("Bulan");
model.addColumn("Jumlah");
model.addColumn("Status");

try {

    String kategori =
    dropdownCari.getSelectedItem().toString();

    String keyword =
    boxCariTagihan.getText();

    if(kategori.equals("Nama")){
        kategori = "nama";
    } else {
        kategori = "nim";
    }

    String sql =
    "SELECT * FROM tagihan WHERE "
    + kategori +
    " LIKE ?";

    pst = con.prepareStatement(sql);

    pst.setString(1, "%" + keyword + "%");

    rs = pst.executeQuery();

    while (rs.next()) {

        model.addRow(new Object[]{

            rs.getString("nama"),
            rs.getString("nim"),
            rs.getString("bulan"),
            rs.getString("jumlah"),
            rs.getString("status"),

        });

    }

    tabelTagihanAdmin.setModel(model);

} catch (Exception e) {

    JOptionPane.showMessageDialog(
        null,
        e.getMessage());

}
    }//GEN-LAST:event_btnCariTagihanActionPerformed

