    private void btnCariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCariActionPerformed
        // TODO add your handling code here:
    DefaultTableModel model = new DefaultTableModel();

    model.addColumn("Nama");
    model.addColumn("Email");
    model.addColumn("NIM");

    try {

        Connection con = DriverManager.getConnection(
                "jdbc:mysql://localhost:3306/gbq",
                "root",
                "admin123"
        );

        String keyword = boxCari.getText();
        String kategori = dropdownCari.getSelectedItem().toString();

        String query = "";

        if (kategori.equals("Nama")) {

            query = "SELECT u.username, u.email, m.nim "
                    + "FROM logingbq u "
                    + "JOIN mahasiswa m ON u.user_id = m.user_id "
                    + "WHERE u.username LIKE ?";

        } else {

            query = "SELECT u.username, u.email, m.nim "
                    + "FROM logingbq u "
                    + "JOIN mahasiswa m ON u.user_id = m.user_id "
                    + "WHERE m.nim LIKE ?";

        }

        PreparedStatement pst = con.prepareStatement(query);

        pst.setString(1, "%" + keyword + "%");

        ResultSet rs = pst.executeQuery();

        while (rs.next()) {

            model.addRow(new Object[]{
                rs.getString("username"),
                rs.getString("email"),
                rs.getString("nim")
            });

        }

        tabelMahasiswa.setModel(model);

    } catch (Exception e) {

        JOptionPane.showMessageDialog(null, e.getMessage());

    }

    }//GEN-LAST:event_btnCariActionPerformed

