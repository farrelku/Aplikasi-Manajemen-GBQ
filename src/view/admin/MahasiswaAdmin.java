    public void tampilData() {
     DefaultTableModel model = new DefaultTableModel();

    model.addColumn("ID");
    model.addColumn("Nama");
    model.addColumn("Email");
    model.addColumn("NIM");

    try {

        Connection con = DriverManager.getConnection(
                "jdbc:mysql://localhost:3306/gbq",
                "root",
                "admin123"
        );

        Statement st = con.createStatement();

        String query = "SELECT u.user_id, u.username, u.email, m.nim "
                + "FROM logingbq u "
                + "JOIN mahasiswa m ON u.user_id = m.user_id "
                + "WHERE u.role='Mahasiswa'";

        ResultSet rs = st.executeQuery(query);

        while (rs.next()) {

            model.addRow(new Object[]{
                rs.getInt("user_id"),
                rs.getString("username"),
                rs.getString("email"),
                rs.getString("nim")
            });

        }

        tabelMahasiswa.setModel(model);

        // sembunyikan kolom ID
        tabelMahasiswa.getColumnModel().getColumn(0).setMinWidth(0);
        tabelMahasiswa.getColumnModel().getColumn(0).setMaxWidth(0);
        tabelMahasiswa.getColumnModel().getColumn(0).setWidth(0);

    } catch (Exception e) {

        JOptionPane.showMessageDialog(null, e.getMessage());

    }
}
    
   
    
    

