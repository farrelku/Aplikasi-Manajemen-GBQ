    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        
        if (selectedAbsensiId == 0) {

        JOptionPane.showMessageDialog(null, "Pilih data absensi dulu");

        return;

    }

    if (jDateChooser1.getDate() == null) {

        JOptionPane.showMessageDialog(null, "Tanggal belum dipilih");

        return;

    }

    SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");

    String tanggal = sdf.format(jDateChooser1.getDate());

    String status = dropdownCari1.getSelectedItem().toString();

    controller.editAbsensi(
            selectedAbsensiId,
            tanggal,
            status
    );

    controller.tampilAbsensi(tabelMahasiswa1);

       

    }//GEN-LAST:event_btnSimpanActionPerformed

