    private void tabelMahasiswa1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelMahasiswa1MouseClicked
        // TODO add your handling code here:
          int baris = tabelMahasiswa1.getSelectedRow();

    selectedAbsensiId = Integer.parseInt(
            tabelMahasiswa1.getValueAt(baris, 0).toString()
    );

    editNama.setText(
            tabelMahasiswa1.getValueAt(baris, 1).toString()
    );

    try {

        java.util.Date tanggal = new java.text.SimpleDateFormat("yyyy-MM-dd")
                .parse(
                        tabelMahasiswa1.getValueAt(baris, 3).toString()
                );

        jDateChooser1.setDate(tanggal);

    } catch (Exception e) {

        JOptionPane.showMessageDialog(null, e.getMessage());

    }

    dropdownCari1.setSelectedItem(
            tabelMahasiswa1.getValueAt(baris, 4).toString()
    );

    }//GEN-LAST:event_tabelMahasiswa1MouseClicked

